    private void calculateData() {
        boolean valid;
            
        if (aCircle == null) {
            createNewCircle();
        }
        String str = InputHelpers.getStringField(txtName, "Circle Name");
        if (!StringHelpers.IsNullOrEmpty(str)) {
            valid = true;
            aCircle.setName(str);
        }
        else {
            valid = false;
        }
        if (valid) {
            double radius = InputHelpers.parseDoubleField(txtRadius, "Circle Radius", Circle.MIN_RADIUS, Circle.MAX_RADIUS);
            if (radius != (double)InputHelpers.CANCEL_FLAG) {
                aCircle.setRadius(radius);
            }
            else {
                valid = false;
            }
        }
        if (valid) {
            int X = InputHelpers.parseIntegerField(txtX, "X Coordinate", Circle.MIN_COORDINATE, Circle.MAX_COORDINATE);
            if (X != InputHelpers.CANCEL_FLAG) {
                aCircle.setX(X);
            }
            else {
                valid = false;
            }
        }
        if (valid) {
            int Y = InputHelpers.parseIntegerField(txtY, "Y Coordinate", Circle.MIN_COORDINATE, Circle.MAX_COORDINATE);
            if (Y != InputHelpers.CANCEL_FLAG) {
                aCircle.setY(Y);
            }
            else {
                valid = false;
            }
        }
        if (valid) {
            txtCircle.setText(aCircle.toString());
        }
    }
